// ternary operator

// using if-else
let age = 15;
let drink;
if (age >= 5) {
  drink = "coffee";
} else {
  drink = "milk";
}
console.log(drink);

//  ternary operator / conditional operator

let age1 = 3;
//  assign    stmt        true       false
let drink1 = age1 >= 5 ? "coffee" : "milk";
console.log(drink1);
