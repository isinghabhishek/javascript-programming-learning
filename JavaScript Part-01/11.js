// Booleans & Comparison Operator

// Booleans -> (true, false)
let num1 = 9;
let num2 = 7;
console.log(num1 <= num2);

// NaN() function
// The is NaN() function returns true if the variable value is not a number.

// The == operator checks equality only
// whereas === checks equality, and data type,
// i.e., a value must be of the same type.

// == vs ===
let num11 = "7";
let num12 = 7;
console.log(num11 == num12); // only value will be check , but in other language both value and data type is check.
console.log(num11 === num12);

// != vs !==
let num111 = "7";
let num112 = 7;
console.log(num111 != num112);
console.log(num111 !== num112);
